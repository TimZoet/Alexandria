#include "common/type_traits.h"

namespace alex
{
    template<typename T>
    concept is_primitive = std::floating_point<T> || std::integral<T>;

    template<auto M>
    concept is_primitive_mp = is_primitive<member_pointer_value_t<decltype(M)>>;
}  // namespace alex
